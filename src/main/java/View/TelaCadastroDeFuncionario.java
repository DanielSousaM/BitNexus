    public void listarFuncionario() {
        List<CadastroDeFuncionarioModel> listaFuncionario = controler.listar();
        DefaultTableModel modelo = (DefaultTableModel) tabelaFuncionario.getModel();
        modelo.setRowCount(0);

        for (CadastroDeFuncionarioModel cdfm : listaFuncionario) {
            modelo.addRow(new Object[]{
                cdfm.getNomeFuncionario(),
                cdfm.getDatanascimentoFuncionario(),
                cdfm.getTelefoneFuncionario(),
                cdfm.getCpfFuncionario(),
                cdfm.getEmailFuncionario(),
                cdfm.getSenhaFuncionario(),
                cdfm.getIdFUNCIONARIOS()
            });
        }
        tabelaFuncionario.setModel(modelo);
      
        // oltutar a coluna do ID 
        tabelaFuncionario.getColumnModel().getColumn(6).setMinWidth(0);
        tabelaFuncionario.getColumnModel().getColumn(6).setMaxWidth(0);
        tabelaFuncionario.getColumnModel().getColumn(6).setWidth(0);    
               
    }
   
    
      
