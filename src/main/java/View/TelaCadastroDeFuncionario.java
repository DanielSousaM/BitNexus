    public void filtrarFuncionario(String textoPesquisaFuncionario){
        List<CadastroDeFuncionarioModel> listaFuncionario = controler.pesquisar(textoPesquisaFuncionario);
        DefaultTableModel modelo = (DefaultTableModel) tabelaFuncionario.getModel();
        modelo.setRowCount(0);
        
            for (CadastroDeFuncionarioModel c : listaFuncionario){
                if (c.getNomeFuncionario().toLowerCase().contains(textoPesquisaFuncionario.toLowerCase()) ||
                    c.getCpfFuncionario().toLowerCase().contains(textoPesquisaFuncionario.toLowerCase())) {
                        
                        modelo.addRow(new Object[]{
                            c.getNomeFuncionario(),
                            c.getDatanascimentoFuncionario(),
                            c.getTelefoneFuncionario(),
                            c.getCpfFuncionario(),
                            c.getEmailFuncionario(),
                            c.getSenhaFuncionario(),
                            c.getIdFUNCIONARIOS()
                          
                        });
                           }
            }
    tabelaFuncionario.setModel(modelo);        
    }
   
    
    
    
