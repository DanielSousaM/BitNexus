    public void filtrarMaquinas(String comboboxPesquisar){
        List<MonitoramentoModel> listarMaquina = controler.pesquisar(comboboxPesquisar);
        DefaultTableModel modelo = (DefaultTableModel)tabelaMonitoramento.getModel();
        modelo.setRowCount(0);
        
        for(MonitoramentoModel m: listarMaquina){
            modelo.addRow(new Object[]{
            m.getIdMAQUINAS(),
            m.getNomeMaquina(),
            m.getTipoMaquina()    
            });
        }
        tabelaMonitoramento.setModel(modelo);
    }
   
   public void listarDadosMaquinas() {
    List<MonitoramentoModel> aparelhos = controler.listarMaquinas();
    DefaultTableModel modelo = (DefaultTableModel) tabelaMonitoramento.getModel();
   modelo.setRowCount(0);
    
    for (MonitoramentoModel m : aparelhos) {
        modelo.addRow(new Object[]{
            m.getIdMAQUINAS(),
            m.getTipoMaquina(),
            m.getNomeMaquina()
        });
    }

    tabelaMonitoramento.setModel(modelo);
    
    // Oculta a coluna do ID
    tabelaMonitoramento.getColumnModel().getColumn(0).setMinWidth(0);
    tabelaMonitoramento.getColumnModel().getColumn(0).setMaxWidth(0);
    tabelaMonitoramento.getColumnModel().getColumn(0).setWidth(0);
} 
    
    private void confirmaraparelhoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmaraparelhoActionPerformed
        // TODO add your handling code here:
        MonitoramentoModel novoAparelho = new MonitoramentoModel();
        
        novoAparelho.setTipoMaquina(tipoaparelho.getSelectedItem().toString());
        novoAparelho.setNomeMaquina(nomeaparelho.getText()); 
     
        boolean result = controler.Cadastrar(novoAparelho);
        
        if (result == true){
            JOptionPane.showMessageDialog(null, "Cadastro de aparelho realizado com sucesso");
        } else {
            JOptionPane.showMessageDialog(null, "Falha ao cadastrar");
        }
        listarDadosMaquinas();
        limparCampos();
    }//GEN-LAST:event_confirmaraparelhoActionPerformed

