    private void editarAparelhoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarAparelhoActionPerformed
        // TODO add your handling code here:
         String nomeMaquina = nomeaparelho.getText();
         String tipoMaquina = tipoaparelho.getSelectedItem().toString();
         
         MonitoramentoModel novoAparelho = new MonitoramentoModel(nomeMaquina, tipoMaquina);
         novoAparelho.setIdMAQUINAS(id);
         //novoAparelho.setNomeMaquina(nomeMaquina);
         //novoAparelho.setTipoMaquina(tipoMaquina);
         
         boolean sucesso = controler.EditarMaquinas(novoAparelho);
         
         if (sucesso) {
             JOptionPane.showMessageDialog(this,"Aparelho atualizado com sucesso!");
             listarDadosMaquinas();
         } else {
             JOptionPane.showMessageDialog(this,"Erro ao atualizar aparelho.");
         }
    }//GEN-LAST:event_editarAparelhoActionPerformed

    private void limparCampos(){
        nomeaparelho.setText("");
        tipoaparelho.setSelectedItem(0);
        id=0;
    }
    
    
